    private void btnAssignDeliveryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignDeliveryActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblOrders.getSelectedRow();
        
        if (selectedRow < 0){
            return;
        }
        
        Order order = (Order)tblOrders.getValueAt(selectedRow, 0);
         if(order.getStatus().equals("delivered")||order.getStatus().equals("order placed")){
            JOptionPane.showMessageDialog(null, "This order has already been delivered");
         return ; }
         if(!order.getStatus().equals("Order Accepted")){
            JOptionPane.showMessageDialog(null, "Please accept the order first");
         return ; }
         
         UserAccount selectedItem = (UserAccount) deliveryManCombo.getSelectedItem();
        
        if (selectedItem == null){
            return;
        }
        order.setStatus("assigned");
        order.setDeliveryManName(selectedItem.getEmployee().getName());  
        order.setDeliveryManUserName(selectedItem.getUsername()); 
        order.setDeliveryManId(selectedItem.getEmployee().getId()); 
        JOptionPane.showMessageDialog(null, "Delivery man assigned successfully");
         return ; 
    }//GEN-LAST:event_btnAssignDeliveryActionPerformed

